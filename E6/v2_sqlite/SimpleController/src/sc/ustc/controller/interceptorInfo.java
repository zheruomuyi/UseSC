package sc.ustc.controller;

public class interceptorInfo {
	private String intercepterName;
	private String intercepterClass;
	private String predo;
	private String afterdo;

	public String getIntercepterName() {
		return intercepterName;
	}
	public void setIntercepterName(String intercepterName) {
		this.intercepterName = intercepterName;
	}
	public String getIntercepterClass() {
		return intercepterClass;
	}
	public void setIntercepterClass(String intercepterClass) {
		this.intercepterClass = intercepterClass;
	}
	public String getPredo() {
		return predo;
	}
	public void setPredo(String predo) {
		this.predo = predo;
	}
	public String getAfterdo() {
		return afterdo;
	}
	public void setAfterdo(String afterdo) {
		this.afterdo = afterdo;
	}

}
